def parse(value):
    if value == 'I':
        return 1
    elif value == 'II':
        return 2
    elif value == 'III':
        return 3
    elif value == 'IV':
        return 4
    elif value == 'V':
        return 5
    elif value == 'VI':
        return 6
    elif value == 'VII':
        return 7
    elif value == 'VIII':
        return 8
